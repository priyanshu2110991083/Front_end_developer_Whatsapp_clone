btn1.addEventListener("click", todo1);
function todo1() {
    const txt1 = input1.value;
    const div1 = document.getElementById("mess");
    const p = document.createElement("p");
    p.textContent = txt1;
    p.style.backgroundColor = "#a3ee91";
    p.style.borderRadius = "8px";
    p.style.marginTop = "5px";
    p.style.padding="7px";
    div1.appendChild(p);
    input1.value = "";
    //just to delete message from whatsapp
    p.addEventListener("dblclick",delP);
    function delP(){
        p.id="pDel";    //just adding id to p tag 
        //in this id display none is applied 
    }
}

